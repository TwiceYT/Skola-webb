function ShowBanan() {
    document.getElementById("banan").style.display = "block";
    document.getElementById("apelsin").style.display = "none";
    document.getElementById("apple").style.display = "none";
  }

  function showApelsin() {
    document.getElementById("banan").style.display = "none";
    document.getElementById("apelsin").style.display = "block";
    document.getElementById("apple").style.display = "none";
  }
  
  function showApple() {
    document.getElementById("banan").style.display = "none";
    document.getElementById("apelsin").style.display = "none";
    document.getElementById("apple").style.display = "block";
  }
  